public class PointEx1Chapter4 {
    protected double x;
    protected double y;

    public PointEx1Chapter4(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;

    }

    public double getY() {
        return y;
    }
}
